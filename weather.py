import tkinter as tk 
import requests
import time

def getWeather(canvas):
    city= textfiled.get() # Returns the entry's current text as a string.
    api= "http://api.openweathermap.org/data/2.5/weather?q=" + city +"&appid=ec228f096c73ed3bbb1eff4cb2bb895d"
    # This is freely available API; JSON file is viewd using Postman
    json_data= requests.get(api).json() # get the jason data
    ## Extracting JSON data

    condition= json_data['weather'][0]['main']
    temp= int(json_data['main']['temp']-273.15) #convert Kelvin data to Celcius
    min_temp= int(json_data['main']['temp_min']-273.15)
    max_temp= int(json_data['main']['temp_max']-273.15)
    pressure= json_data['main']['pressure']
    humidity= json_data['main']['humidity']
    wind = json_data['wind']['speed']
    sunrise= time.strftime("%H:%M:%S", time.gmtime(json_data['sys']['sunrise']-21600))
    sunset= time.strftime("%H:%M:%S", time.gmtime(json_data['sys']['sunset']-21600))

    ## Write the extracted variable in the form of a string
    final_info = condition + "\n" + str(temp) + "degree C"
    final_data= "\n" + "Max Temp: " + str(max_temp) + "\n" + "Min Temp: " + str(min_temp) + "\n" + "Pressure:" + str(pressure) + "\n" + "Humidity: " + "\n" + str(humidity) + "Wind Speed: " + "\n" + str(wind) + "\n" + "Sunrise Time: " + str(sunrise) + "\n" + "Sunset Time: " + "\n" + str(sunset) + "\n"
    # Finally pack the output to the labels
    label1.config(text= final_info)
    label2.config(text= final_data)


canvas= tk.Tk() # Tk inter class instantiation

canvas.geometry("600x500") # define the geometry of the Tk class interface
canvas.title("Weather App") # Give the title to the inteface

f= ("poppins", 15, "bold")
t= ("poppins", 35, "bold")

textfiled= tk.Entry(canvas, font = t) # to get the city name
textfiled.pack(pady= 20)
textfiled.focus()
textfiled.bind('<Return>', getWeather) # everytime user enter an input, call the getWeather function

label1= tk.Label(canvas, font = t)
label1.pack()
label2= tk.Label(canvas, font= f)
label2.pack()

canvas.mainloop() # Execution of the python program halts here.

